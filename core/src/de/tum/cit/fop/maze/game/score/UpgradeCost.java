package de.tum.cit.fop.maze.game.score;
/**
 * Defines constant score costs related to ability and upgrade purchases.
 *
 * <p>This class serves as a centralized holder for upgrade-related
 * scoring values and is not intended to be instantiated.
 */
public final class UpgradeCost {
    /**
     * Base score cost required for a single upgrade.
     */
    public static final int SCORE_PER_UPGRADE = 1000;

    /**
     * Private constructor to prevent instantiation.
     */
    private UpgradeCost() {}
}
